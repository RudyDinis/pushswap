#include "../push_swap.h"

void	ft_pa(t_pile **a, t_pile **b)
{
	t_pile	*tmp;

	if (!b || !*b)
		return ;
	tmp = *b;
	*b = (*b)->next;
	tmp->next = *a;
	*a = tmp;
	write(1, "pa\n", 3);
}
