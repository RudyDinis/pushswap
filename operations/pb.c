#include "../push_swap.h"

void	ft_pb(t_pile **a, t_pile **b)
{
	t_pile	*tmp;

	if (!a || !*a)
		return ;
	tmp = *a;
	*a = (*a)->next;
	tmp->next = *b;
	*b = tmp;
}
